// Que: Convert Celcius to farenheit

package celciusToFarenheit;

public class Farenheit {
	public static void main(String[] args) {
		double celcius = 50;
		double fareinheit = 0;
		double n = 9.0 / 5.0;
		int num = 32;
		fareinheit = (celcius * n) + num;
		System.out.println(fareinheit + " F ");
	}

}
